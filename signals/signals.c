#include "minishell.h"

void	sig_handler(int sig)
{
	if (sig == SIGINT)
	{
		write(1, "\n", 1);
		write(1, "HAHAHAHA", 1);
	}
}

void	add_key_signals(void)
{
	struct	sigaction sig;

	sig.sa_handler = &sig_handler;
	sigaction(SIGINT, &sig, NULL);
}